import mongoose from 'mongoose'
const Schema = mongoose.Schema

const UsersSchema = new Schema({
    firstname: {
        type: String,
        required: true,
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    country: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
});

const Users = mongoose.model('Users', UsersSchema);

export default Users;